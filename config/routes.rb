  mount RailsAdmin::Engine => '/admin', as: 'rails_admin'
  mount Rubyrpg::BaseApi => '/api'
  mount GrapeSwaggerRails::Engine, at: '/apidoc'

  get 'about' => 'frontpage#about'
  get 'game' => 'frontpage#game'
